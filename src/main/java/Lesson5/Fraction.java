public class Fraction {
    private int m,n;

    public Fraction(int m, int n) {
        this.m = m;
        this.n = n;
    }

    public void setM(int m) {
        this.m = m;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getM() {
        return m;
    }

    public int getN() {
        return n;
    }
}
